<h2>Hilfe</h2>
<h4>Allgemein</h4>
<p>
  Bei der PictureCloud handelt es sich um eine canvasloudbasierte Lösung um Fotos an einem Ort hochzuladen und zu verwalten.

  Dabei liegt ein Fokus auf der Speicherung von Zusatzinfos der Bilder wie zB Koordinaten oder Aufnahmedatum.

</p>

<h4>Usability</h4>

<p>
  Grundsätzlich sind die Bezeichnungen der Buttons und Links so gewählt, dass nachvollziebar ist was passiert wenn dieser gedrückt wird.

  Die exotischste Bedienoperation sind wohl die Listen (Filterung nach Tags; Liste von Freigaben; Liste von Tags) da hier die Listenelemente drückbar sind und ein löschen dieses auslösen.
</p>

